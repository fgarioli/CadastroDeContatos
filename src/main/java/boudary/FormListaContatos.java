    public void listarContatosPorCodigoNomeTelefone() {
        int cod = 0;
        if (tfCodigo.getText().length() != 0) {
            cod = Integer.parseInt(tfCodigo.getText());
        }

        Contato contato = new Contato();
        contato.setCodigo(cod);
        contato.setNome(tfNome.getText());
        contato.setTelefone(getTel(tfTelefone.getText()));
        contato.setFax(getTel(tfTelefone.getText()));
        contato.setCelular(getTel(tfTelefone.getText()));

        contatos = (ArrayList<Contato>) Control.selecionarTodosContatosPorCodigoNomeTelefone(contato);

//        tableModelContato.setData(contatos);
        tableModel.setData(contatos);
        setCount(contatos.size());
    }

    public void listarPorTelefone(String tel) {
        Contato contato = new Contato();
        contato.setCodigo(0);
        contato.setNome("");
        contato.setTelefone(getTel(tel));
        contato.setFax(getTel(tel));
        contato.setCelular(getTel(tel));

        contatos = (ArrayList<Contato>) Control.selecionarTodosContatosPorCodigoNomeTelefone(contato);

//        tableModelContato.setData(contatos);
        tableModel.setData(contatos);
        setCount(contatos.size());
    }

    private void setCount(int count) {
        lbCount.setText("  Total de Registros: " + count);
    }

    private String getTel(String tel) {
        tel = tel.replace("(", "");
        tel = tel.replace(")", "");
        tel = tel.replace("-", "");
        tel = tel.replace(" ", "");
        tel = tel.trim();

        return tel;
    }

