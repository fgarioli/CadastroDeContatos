    public boolean logarSistema(String login, String senha) {
        try {
            // Abrinco conexÃ£o Hibernate com o banco de dados
            Session session = CriadorDeSession.getSession();

            if (session.isOpen()) {
                Control.setSession(session); // Setando a session na controladora

                Usuario usuario = new Usuario();
                usuario = Control.logarSistema(login, senha);
                if (usuario == null) {
                    JOptionPane.showMessageDialog(null, "Seu usuÃ¡rio e(ou) senha estÃ£o incorretos.\nPor favor digite-os corretamente.", "Erro!", JOptionPane.ERROR_MESSAGE);
                    return false;
                } else {
                    Control.setUsuario(usuario);
//                    this.setarUsuario(usuario);
                    this.setVisible(true);
                    //codigo para deixar maximizado
//                    this.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    preparaTabela();
                    return true;
                }
            } else {
                return false;
            }
        } catch (JDBCConnectionException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conexÃ£o com o banco de dados!\nVerifique se o serviÃ§o estÃ¡ iniciado ou se o endereÃ§o IP do servidor estÃ¡ correto.", "Erro!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private void preparaTabela() {
        contatos = (ArrayList<Contato>) Control.selecionarTodosContatos();
        AnnotationResolver resolver = new AnnotationResolver(Contato.class);

        tableModel = new ObjectTableModel(resolver, "codigo,nome,telefone,fax,celular");

        tableModel.setData(contatos);
        tbContatos.setModel(tableModel);

//        tableModelContato = new ContatoTableModel(contatos);
//
//        tbContatos.setModel(tableModelContato);

        setCount(contatos.size());
        formataColunas();
    }

