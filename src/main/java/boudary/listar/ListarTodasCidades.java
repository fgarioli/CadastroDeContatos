    public void listarCidadesPorCodigoNome() {
        int cod = 0;
        if (tfCodigo.getText().length() != 0) {
            cod = Integer.parseInt(tfCodigo.getText());
        }

        Cidade cidade = new Cidade();
        cidade.setCodCidade(cod);
        cidade.setNomeCidade(tfNome.getText());
//        cidade.setUf((String) cbUF.getSelectedItem());
        
        cidades = (ArrayList<Cidade>) Control.selecionarTodasCidadesPorCodigoNome(cidade);

        tableModel.setData(cidades);
        setCount(cidades.size());
    }
    
    private void setCount(int count) {
        lbCount.setText("  Total de Registros: " + count);
    }
    
    private void formataColunas() {
        tbCidades.setSelectionMode(ListSelectionModel.SINGLE_SELECTION); // permite a seleÃ§Ã£o de somente uma linha
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        tbCidades.getColumnModel().getColumn(0).setCellRenderer(direita);

        tbCidades.getColumnModel().getColumn(0).setPreferredWidth(45);
        tbCidades.getColumnModel().getColumn(1).setPreferredWidth(380);
        tbCidades.getColumnModel().getColumn(2).setPreferredWidth(90);
    }
    
    private void preparaTabela() {
        cidades = (ArrayList<Cidade>) Control.selecionarTodasCidades();
        AnnotationResolver resolver = new AnnotationResolver(Cidade.class);

        tableModel = new ObjectTableModel(resolver, "codCidade,nomeCidade,uf");

        tableModel.setData(cidades);
        tbCidades.setModel(tableModel);

        setCount(cidades.size());
        formataColunas();
    }
    
